CREATE TABLE SERVICIOS_PRIVADOS 
(
  ID NUMBER NOT NULL 
, NOMBRE VARCHAR2(20) NOT NULL 
, CONSTRAINT SERVICIOS_PRIVADOS_PK PRIMARY KEY 
  (
    ID 
  )
  ENABLE 
);

CREATE SEQUENCE SERVICIOS_PRIVADOS_SEQ;

CREATE TRIGGER SERVICIOS_PRIVADOS_TRG 
BEFORE INSERT ON SERVICIOS_PRIVADOS 
FOR EACH ROW 
BEGIN
  <<COLUMN_SEQUENCES>>
  BEGIN
    IF INSERTING AND :NEW.NOMBRE IS NULL THEN
      SELECT SERVICIOS_PRIVADOS_SEQ.NEXTVAL INTO :NEW.NOMBRE FROM SYS.DUAL;
    END IF;
  END COLUMN_SEQUENCES;
END;
/
